function dwsScreenPlay:notifyTriggerDead(pTrigger, pPlayer)

    local pBoss = spawnMobile("rori", "golga_the_hutt", 0, -101.4, -50.0, 55.7, 86, 3566259)
	ObjectManager.withCreatureObject(pBoss, function(oBoss)
		writeData("dwsScreenPlay:spawnState", 1)
		writeData("golgaboss", oBoss:getObjectID())	
		spatialChat(pBoss, "Karking schutta poodoo Ho Ho Ho")	
		createObserver(DAMAGERECEIVED,"dwsScreenPlay","boss_damage", pBoss)
		createObserver(OBJECTDESTRUCTION, "dwsScreenPlay", "notifyGolgaDead", pBoss)
		createEvent(1800000, "dwsScreenPlay", "despawnBoss", pBoss)		
	end)
	return 0
end

