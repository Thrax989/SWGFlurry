	writeData("dwsScreenPlay:spawnState", 0)
	return 0
end

function dwsScreenPlay:despawnBoss(pBoss, pPlayer)
	forcePeace(pBoss)
	spHelper.destroy(readData("golgaboss"),true)
	writeData("dwsScreenPlay:spawnState", 0)
	return 0
end

